result = 10 - 2  # 減算
puts result     # 結果: 8

result = 4 * 6  # 乗算
puts result    # 結果: 24

result = 20 / 5  # 除算
puts result     # 結果: 4

result = 2 ** 3  # 2の3乗
puts result      # 結果: 8

result = 10 ** 2  # 10の2乗
